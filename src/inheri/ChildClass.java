package inheri;

public class ChildClass extends ParentClass{

//	Inheritance: It is inheriting the properties of parent class into 
//	child class is known as Inheritance in Java
	
//	MethodOverriding - When method is same in parent class as well as in child class 
//	with same input parameters is known as method overriding.
	
	public void bike() {
		System.out.println("Child Class - Bike");
	}

	public void bycycle() {
		System.out.println("Child Class - Bycycle");
	}
}
